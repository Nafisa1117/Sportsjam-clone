function getAllCategories(arr){
    var uniq = {};
    for(var i =0; i<arr.length; i++){
        if(uniq[arr[i].category]===undefined){
            uniq[arr[i].category] =1;
        }
        else{
            uniq[arr[i].category]++;
        }        
    }
    console.log(uniq)
    return uniq;
}

